# This shell is used to auto generate some useful tools for k8s, such as lister,
# informer, client and so on.

set -o errexit
set -o nounset
set -o pipefail

GOPATH=$(go env GOPATH)
export GOPATH
GOBIN="${GOPATH}/bin"

SCRIPT_ROOT=$(dirname "${BASH_SOURCE[0]}")/..
ROOT_PKG=github.com/koordinator-sh/koordinator
BOILERPLATE="${SCRIPT_ROOT}/hack/boilerplate/boilerplate.go.txt"

# Allow 'go install' to fetch tools outside vendor during codegen.
export GOFLAGS="-mod=mod"

# Install required codegen tools from the vendored code-generator module.
echo ">> Installing codegen tools"
(
    cd "${SCRIPT_ROOT}"
    go install \
        k8s.io/code-generator/cmd/client-gen \
        k8s.io/code-generator/cmd/lister-gen \
        k8s.io/code-generator/cmd/informer-gen \
        k8s.io/code-generator/cmd/deepcopy-gen \
        k8s.io/code-generator/cmd/conversion-gen \
        k8s.io/code-generator/cmd/defaulter-gen
)

# ---------------------------------------------------------------------------
# External API groups: client / lister / informer
# We explicitly list only our own API packages to avoid picking up
# apis/thirdparty which carries its own scheduling/v1alpha1 group.
# ---------------------------------------------------------------------------
INPUT_BASE="${ROOT_PKG}/apis"
API_PKGS=(
    "${INPUT_BASE}/analysis/v1alpha1"
    "${INPUT_BASE}/config/v1alpha1"
    "${INPUT_BASE}/slo/v1alpha1"
    "${INPUT_BASE}/scheduling/v1alpha1"
    "${INPUT_BASE}/quota/v1alpha1"
)

# client-gen uses --input <group/version> relative to --input-base
CLIENT_INPUTS=(
    "analysis/v1alpha1"
    "config/v1alpha1"
    "slo/v1alpha1"
    "scheduling/v1alpha1"
    "quota/v1alpha1"
)

CLIENT_PKG="${ROOT_PKG}/pkg/client"

echo ">> Generating client code"
# Remove old generated client files before re-generating
find "${SCRIPT_ROOT}/pkg/client/clientset" -name "*.go" \
    -exec grep -l "^// Code generated by client-gen" {} \; \
    | xargs -r rm -f

INPUT_FLAGS=()
for gv in "${CLIENT_INPUTS[@]}"; do
    INPUT_FLAGS+=("--input" "${gv}")
done

"${GOBIN}/client-gen" \
    --go-header-file "${BOILERPLATE}" \
    --output-dir "${SCRIPT_ROOT}/pkg/client/clientset" \
    --output-pkg "${CLIENT_PKG}/clientset" \
    --clientset-name "versioned" \
    --input-base "${INPUT_BASE}" \
    --fake-clientset \
    "${INPUT_FLAGS[@]}"

echo ">> Generating lister code"
find "${SCRIPT_ROOT}/pkg/client/listers" -name "*.go" \
    -exec grep -l "^// Code generated by lister-gen" {} \; \
    | xargs -r rm -f

"${GOBIN}/lister-gen" \
    --go-header-file "${BOILERPLATE}" \
    --output-dir "${SCRIPT_ROOT}/pkg/client/listers" \
    --output-pkg "${CLIENT_PKG}/listers" \
    "${API_PKGS[@]}"

echo ">> Generating informer code"
find "${SCRIPT_ROOT}/pkg/client/informers" -name "*.go" \
    -exec grep -l "^// Code generated by informer-gen" {} \; \
    | xargs -r rm -f

"${GOBIN}/informer-gen" \
    --go-header-file "${BOILERPLATE}" \
    --output-dir "${SCRIPT_ROOT}/pkg/client/informers" \
    --output-pkg "${CLIENT_PKG}/informers" \
    --versioned-clientset-package "${CLIENT_PKG}/clientset/versioned" \
    --listers-package "${CLIENT_PKG}/listers" \
    "${API_PKGS[@]}"

# ---------------------------------------------------------------------------
# Scheduler internal config types: deepcopy / defaulter / conversion
# ---------------------------------------------------------------------------
SCHEDULER_CONFIG_PKGS=(
    "${ROOT_PKG}/pkg/scheduler/apis/config"
    "${ROOT_PKG}/pkg/scheduler/apis/config/v1"
)
DESCHEDULER_CONFIG_PKGS=(
    "${ROOT_PKG}/pkg/descheduler/apis/config"
    "${ROOT_PKG}/pkg/descheduler/apis/config/v1alpha2"
)

echo ">> Generating deepcopy for scheduler config types"
"${GOBIN}/deepcopy-gen" \
    --go-header-file "${BOILERPLATE}" \
    --output-file "zz_generated.deepcopy.go" \
    "${SCHEDULER_CONFIG_PKGS[@]}"

echo ">> Generating deepcopy for descheduler config types"
"${GOBIN}/deepcopy-gen" \
    --go-header-file "${BOILERPLATE}" \
    --output-file "zz_generated.deepcopy.go" \
    "${DESCHEDULER_CONFIG_PKGS[@]}"

echo ">> Generating defaulter for scheduler config types"
"${GOBIN}/defaulter-gen" \
    --go-header-file "${BOILERPLATE}" \
    --output-file "zz_generated.defaults.go" \
    "${ROOT_PKG}/pkg/scheduler/apis/config/v1"

echo ">> Generating defaulter for descheduler config types"
"${GOBIN}/defaulter-gen" \
    --go-header-file "${BOILERPLATE}" \
    --output-file "zz_generated.defaults.go" \
    "${ROOT_PKG}/pkg/descheduler/apis/config/v1alpha2"

echo ">> Generating conversion for scheduler config types"
"${GOBIN}/conversion-gen" \
    --go-header-file "${BOILERPLATE}" \
    --output-file "zz_generated.conversion.go" \
    --extra-peer-dirs "k8s.io/apimachinery/pkg/apis/meta/v1,k8s.io/apimachinery/pkg/conversion,k8s.io/apimachinery/pkg/runtime" \
    "${ROOT_PKG}/pkg/scheduler/apis/config/v1"

echo ">> Generating conversion for descheduler config types"
"${GOBIN}/conversion-gen" \
    --go-header-file "${BOILERPLATE}" \
    --output-file "zz_generated.conversion.go" \
    --extra-peer-dirs "k8s.io/apimachinery/pkg/apis/meta/v1,k8s.io/apimachinery/pkg/conversion,k8s.io/apimachinery/pkg/runtime" \
    "${ROOT_PKG}/pkg/descheduler/apis/config/v1alpha2"

echo ">> Done."
